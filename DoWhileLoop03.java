// Do While Loop
// Print My Name 5 Time By Using Do_While_Loop.

public class DoWhileLoop03 {

	public static void main(String[] args) {
		 
		int a = 1;
		
		// do While Loop
		do {
			// Print My Full Name
			System.out.println("Siddiqui Huzefa Muzammil.");
			// Increment 
			 a++;
		} while (a<5);
		  
		

	}

}
