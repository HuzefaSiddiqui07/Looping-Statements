// For Loop Type
// Create Maths Table Of '3' With the help of For_Loop.


public class ForLoop06 {

	public static void main(String[] args) {
		 int fix_num = 3;
		 int range = 10;
		 int k=1;
		for (; k<=range; k++) {
			System.out.println(fix_num+" * "+k+" = "+fix_num*k); // Formula for create maths table
		}
	}

}
